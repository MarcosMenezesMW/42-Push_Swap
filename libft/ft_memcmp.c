#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;
	int				len;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	len = 0;
	if (n == 0 || str1 == str2)
		return (len);
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (len);
}
